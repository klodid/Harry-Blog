# Create admin-only Decorator
def admin_only(f):
    @wraps(f)
    def decorated_function(*args, **kwargs):
        if current_user.is_authenticated and current_user.id == 1:
            return f(*args, **kwargs)
        else:
            return abort(403)

    return decorated_function


@app.route('/')
def get_all_posts():
    posts = BlogPost.query.all()
    if not current_user.is_authenticated:
        current_user.id = 0
    return render_template("index.html", all_posts=posts,
                           logged_in=current_user.is_authenticated, user_id=current_user.id)


@app.route('/register', methods=["POST", "GET"])
def register():
    register_form = RegisterForm()
    if register_form.validate_on_submit():
        email = register_form.email.data
        password = register_form.password.data
        name = register_form.name.data
        if User.query.filter_by(email=email).first():
            flash("You've already signed up with that email, log in instead!")
            return redirect(url_for("login"))
        else:
            hashed_and_salted_pass = generate_password_hash(password, method="pbkdf2:sha256", salt_length=8)
            new_user = User(email=email, password=hashed_and_salted_pass, name=name)
            db.session.add(new_user)
            db.session.commit()
            login_user(new_user)
            return redirect(url_for("get_all_posts"))
    return render_template("register.html", form=register_form, logged_in=current_user.is_authenticated)


@app.route('/login', methods=["GET", "POST"])
def login():
    login_form = LoginForm()
    if login_form.validate_on_submit():
        user = User.query.filter_by(email=login_form.email.data).first()
        if user and check_password_hash(user.password, login_form.password.data):
            login_user(user)
            return redirect(url_for("get_all_posts"))
        else:
            flash("Email or Password incorrect, please try again.")
            return redirect("login")
    return render_template("login.html", form=login_form, logged_in=current_user.is_authenticated)


@app.route('/logout')
@login_required
def logout():
    logout_user()
    return redirect(url_for('get_all_posts'))


@app.route("/post/<int:post_id>", methods=["GET", "POST"])
def show_post(post_id):
    requested_post = BlogPost.query.get(post_id)
    comment_form = CommentForm()
    if not current_user.is_authenticated:
        current_user.id = 0
        current_user.email = ""

    if comment_form.validate_on_submit():
        if not current_user.is_authenticated:
            flash("You need to Login to register a comment.")
            return redirect(url_for("login"))

        new_comment = Comment(
            author_id=current_user.id,
            post_id=post_id,
            text=comment_form.text.data
        )
        db.session.add(new_comment)
        db.session.commit()
        return redirect(url_for("show_post", post_id=post_id))

    return render_template("post.html", post=requested_post, user_id=current_user.id, form=comment_form,
                           logged_in=current_user.is_authenticated)


